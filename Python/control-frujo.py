# Control de frujo
a = 0

# Condicionales
if a > 0:
    print('El numero es mayor.')

elif a == 0:
    print('El numero es neutral.')

else:
    print('El numero es negativo.')
 
# Bucle while   
while a <= 5:
    print(a)
    a += 1

# Bucle for    
numeros = [1,2,3,4,5]
for n in numeros:
    print(n)